    <h3>What We Do</h3>
    <p>
        We undertake the role of verifying each and every member registered with us, either as a lender or borrower. For
        this purpose, we collect personal, professional and financial details from each potential member and
        authenticate it. The list of information collected is available in the registration section. All the information
        collected is for official purposes only and we safeguard client confidentiality.
    </p>
    <p>
        Once a borrower and a lender come to a mutual agreement, Funding will help them legalize the transaction by
        getting a formal contract signed by the lender and borrower. We make necessary provisions and help both parties
        realize the deal without the necessity of a face to face meeting.
    </p>
    <p>
        Once a loan is disbursed, we help lenders with the collection and recovery of loans. Delay in repayment by
        borrowers draw a penalty which is pursued by Funding as part of the process.
    </p>
    <hr>

    <h3>What We Do Not Do</h3>

    <p>
        Decisions to lend on this portal are entirely at the discretion of potential lenders. We do not guarantee fixed
        or minimum rate of returns to any lender. Nor do we guarantee the principal amount to lenders. We facilitate the
        decision making process by providing relevant information about the borrowers and lenders to each other.
    </p>
    <p>
        We encourage all borrowers and lenders to read the policy and guidelines on the website clearly before
        registering and contact us for any clarifications. For further details, please read Funding Policy and Terms
        and Conditions
    </p>
    <p>
        As part of our commitment to remain neutral and not intervene in the decision making process. However, we do
        provide the interest rate at which each borrower could be funded as per the Funding risk analysis.
    </p>
    Following our philosophy of being a neutral platform, we do not give any financial advice or recommendations to
    either borrowers or lenders on this forum. Members are encouraged to make their own informed decisions and
    transact accordingly, free of any coercion or any form of influence from us.
    </p>

